package dataTypes;

public class Comparision {
public static void main (String [] args) {
	int number1 = 15;
	int number2 = 25;
	boolean greater = number1 > number2;
	boolean lessThan = number1 < number2;
	boolean equal = number1 == number2;
	System.out.println ("Greater = " + greater + ", Less Than = " + lessThan + ", Equal = " + equal);
}
}
